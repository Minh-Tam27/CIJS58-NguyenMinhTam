//TOÁN TỬ TOÁN HỌC
console.log("+ :", 1 + 1);
console.log("- :", 1 - 1);
console.log("* :", 1 * 1);
console.log("/ :", 1 / 1);
console.log("** :", 2 ** 2); //lũy thừa
console.log("% :", 3 % 2); //chia lấy dư

//TOÁN TỬ LOGIC
console.log("&& :", true && false);
console.log("|| :", true || false);
console.log(true && 'hello'); //hello
console.log("hello" || "world");

//TOÁN TỬ SO SÁNH
console.log("> :", 1 > 0);
console.log(">= :", 1 >= 0);
console.log("> :", 1 < 0);
console.log(">= :", 1 <= 0);
console.log("== :", 1 == "1"); //T: value của string
console.log("===", 1 === "1"); //F
console.log("!=", 1 != 0);
console.log("> :", "hello" > "world"); //F: sắp xếp theo bảng c chữ cái

